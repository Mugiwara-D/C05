int		ft_is_prime(int nb)
{
	if (nb <= 1 && nb >= -1)
		return (0);
	else if ((nb % 2) == 1)
		return (1);
	else
		return (0);
}
#include <stdio.h>
int main()
{
	printf("%d\n",ft_is_prime(-13));
}
